#include<stdio.h>
/**
 *  add two integers
 *  #a: first int
 *  #b: second int
 **/

int add(int a, int b){
	return (a + b);
}

/**
 *  modulus of two integers
 *  #a: first int
 *  #b: second int
 **/

int mod(int a, int b){
        return (a % b);
}

/**
 *  subtract two integers
 *  #a: first int
 *  #b: second int
 **/

int sub(int a, int b){
        return (a - b);
}

/**
 *  divide two integers
 *  #a: first int
 *  #b: second int
 **/

int divide(int a, int b){
        return (a / b);
}
/**
 *  multiply two integers
 *  #a: first int
 *  #b: second int
 **/

int mul(int a, int b){
	return (a * b);
}
